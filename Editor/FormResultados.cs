        public void MuestraGrids(DataSet ds, string Msg)
        {
            DS = ds;
            toolStrip1.Visible = true;
            Grids = new List<Cid_Utilities.Grid.CidGrid>();//DataGrid>();
            int i, n;
            n = 0;// ds.Tables.Count;
            foreach (DataTable dt in ds.Tables)
            {
                if (dt.Columns.Count > 0)
                    n++;
            }
            for (i = 0; i < n; i++)
            {
                Cid_Utilities.Grid.CidGrid dataGrid1 = new Cid_Utilities.Grid.CidGrid();
                dataGrid1.Titulo = Msg;
//                System.Windows.Forms.DataGrid dataGrid1;
//                dataGrid1 = new System.Windows.Forms.DataGrid();
//                dataGrid1.DataMember = "";
                if (i == n - 1)
                    dataGrid1.Dock = System.Windows.Forms.DockStyle.Fill;
                else
                    dataGrid1.Dock = System.Windows.Forms.DockStyle.Top;
//                dataGrid1.HeaderForeColor = System.Drawing.SystemColors.ControlText;
                dataGrid1.Name = "dataGrid1";
                dataGrid1.Ordenamiento = true;
//                dataGrid1.ReadOnly = true;
                dataGrid1.Height = 150;
                dataGrid1.DataSource = ds.Tables[i];
                PanelPrincipal.Controls.Add(dataGrid1);
                Grids.Add(dataGrid1);
                if (i < n - 1)
                {
                    System.Windows.Forms.Splitter splitter = new Splitter();
                    splitter.Dock = System.Windows.Forms.DockStyle.Top;
                    splitter.Size = new System.Drawing.Size(PanelPrincipal.Width, 3);
                    splitter.Location = new System.Drawing.Point(PanelPrincipal.Width, dataGrid1.Top + dataGrid1.Height + 10);
                    PanelPrincipal.Controls.Add(splitter);
                }
            }
            n = PanelPrincipal.Controls.Count;
            for (i = n - 1; i >= 0; i--)
            {
                PanelPrincipal.Controls[i].SendToBack();
            }
        }

