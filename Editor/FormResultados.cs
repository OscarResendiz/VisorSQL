        private void TExportar_Tick(object sender, EventArgs e)
        {
            switch (Estado)
            {
                case 0://estado en que se tare un grid;
                    if (PosGrids >= Grids.Count)
                    {
                        //ya termino de recorrer todos los grids
                        TExportar.Enabled = false;
                        GeneradorExcel.Termina_Excel();
                        System.Windows.Forms.MessageBox.Show("El archivo se ha creado satisfactoriamente");
                        BGUardar.Enabled = true;
                        cBarraProgreso1.Visible = false;
                        return;
                    }
                    //me traigo el grid;
                    Grid = Grids[PosGrids];
                    //preparo el proximo grid
                    PosGrids++;
                    //le asigno el nombre a la hoja de calculo       
                    //creo el archivo
                    string sNombreHoja = saveFileDialog1.FileName;
                    NombreHoja = "";
                    //le copio la ruta y nombre del archivo
                    int i, n;
                    n = sNombreHoja.Length;
                    for (i = 0; i < n && sNombreHoja[i] != '.'; i++)
                    {
                        NombreHoja = NombreHoja + sNombreHoja[i];
                    }
                    //veo si tengo que agregarle numerode archivo
                    if (PosGrids > 1)
                    {
                        NombreHoja = NombreHoja + PosGrids.ToString();
                    }
                    //ahora me traigo la extencion
                    string ext = "";
                    for (; i < n; i++)
                    {
                        if(sNombreHoja[i]!='.')
                        ext = ext + sNombreHoja[i];
                    }
                    if (ext.Trim() == "")
                    {
                        ext = "xls";
                    }
                    NombreHoja = NombreHoja + "." + ext;
                    //creo el generador de excell
                    //Tabla = (DataTable)Grid.DataSource;
                    GeneradorExcel = new Generador_Excel(NombreHoja, Tabla.Rows.Count);
                    //recorro las columnas para irle poniendo los titulos
                    GeneradorExcel.Inicia_Excel(NombreHoja);
                    Columnas = Tabla.Columns.Count;
                    List<string> titulos = new List<string>();
                    for ( i = 0; i < Columnas; i++)
                    {
                        //agrego el titulo
                        titulos.Add(Tabla.Columns[i].ColumnName);
                    }
                    GeneradorExcel.EscribeLinea(titulos, false, 7);
                    Registros = Tabla.Rows.Count;
                    NunReg = 0;
                    Estado = 1;
                    break;
                case 1:
                    //guardando los registros en la hoja
                    if (NunReg >= Registros)
                    {
                        //ya termine de esta tabla, que se pase a la siguiente
                        Estado = 0;
                        return;
                    }
                    List<string> datos = new List<string>();
                    for ( i = 0; i < Columnas; i++)
                        {
                            //agrego el titulo
                            string col = DameColumnaExcel(i);
                            string dato = Tabla.Rows[NunReg][i].ToString();
                            datos.Add(dato);
                        }
                                GeneradorExcel.EscribeLinea(datos, false, 0);
                        NunReg++;
                        cBarraProgreso1.Progreso++;
                        if (cBarraProgreso1.Visible == false)
                            cBarraProgreso1.Visible = true;
                        cBarraProgreso1.Texto = NombreHoja+" "+cBarraProgreso1.Progreso.ToString() + "/" + cBarraProgreso1.ValorMaximo.ToString();
                    break;
            }
        }

